const contract = new web3.eth.Contract(abi, contractAddress);

// Replace these with your wallet details
const account = '0xdc10471e56D6a75FeBb540eB87865e586b03cA22';
const privateKey = process.env.PRIVATE_KEY;  // Never expose private keys in production!

// Example function to call a contract method (like depositing into the group)
async function mint(mintAddress, amount) {
    const usdcAmount = amount * 10 ** 6
    try {
        // Estimate gas
        const gasEstimate = await contract.methods
            .mint(mintAddress, usdcAmount)
            .estimateGas({ from: account });
        
        // Create transaction object
        const tx = {
            from: account,
            to: contractAddress,
            gas: gasEstimate,
            data: contract.methods.mint(mintAddress, usdcAmount).encodeABI(),
        };

        // Sign the transaction
        const signedTx = await web3.eth.accounts.signTransaction(tx, privateKey);

        // Send the transaction
        const receipt = await web3.eth.sendSignedTransaction(signedTx.rawTransaction);

        console.log('Transaction successful with hash:', receipt.transactionHash);
    } catch (error) {
        console.error('Transaction failed:', error);
    }
}

// Example function to call a contract method (like depositing into the group)
async function balanceOf(address) {
    try {
        const balance = await contract.methods.balanceOf(address).call();

        console.log(`balanceOf(${address}) = ${balance / 10 ** 6}`);
    } catch (error) {
        console.error('Transaction failed:', error);
    }
}
